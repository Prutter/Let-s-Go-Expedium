let el = (tag)=>{
	return document.createElement(tag);
}

let renderDom = (data)=>{
	let container = document.querySelector(".container")
	data.forEach((ele)=>{	
		let div1 = el("div");
		let image = el("img");
		image.src = ele["photos"][1];
		let s1 = el("span");
		s1.innerHTML = "Ad"
		s1.setAttribute("class","arrow adv")
		let s2 = el("span");
		s2.innerHTML = "&#10094;"
		s2.setAttribute("class","arrow prev")
		let j;
		j= ele['i']
		s2.onclick= ()=>{
			console.log(j);
			if(j==0)
			j=3;
			image.src= ele["photos"][j];
			j=j-1 

			ele['i'] = j
		}
		let s3 = el("span");
		s3.innerHTML = "&#10095;"
		s3.setAttribute("class","arrow next")
		s3.onclick= ()=>{
			console.log(j);
			j=j +1;
			if(j>=4)
			j=0;
			image.src= ele["photos"][j];
			ele['i'] = j;
		}
		let s4 = el("span");
		s4.innerHTML = "&#9825;"
		s4.setAttribute("class","heart");

		let div2 = el("div");
		let title = el("h1");
		title.setAttribute("class","title")
		title.innerHTML = ele.title;
		let location = el("h3");
		location.setAttribute("class","location")
		location.innerHTML = ele.location
		let desc = el("p");
		desc.setAttribute("class","description")
		desc.innerHTML = ele.description
		let rating = el("h2");
		rating.setAttribute("class","rating");
		rating.innerHTML = ele.rating

		let div3 = el("div");
		let off = el("button")
		off.innerHTML = ele.offer;
		off.setAttribute("class","offer")
		let pr = el("h1")
		pr.innerHTML = '&#8377; '+ele.price
		pr.setAttribute("class","price");
		let include = el("p");
		include.innerHTML = "Includes All Taxes And Charges.";

		div1.append(image,s1,s2,s3,s4);
		div1.setAttribute("class","image");
		div2.append(title,location,desc,rating);
		div2.setAttribute("class","div2")
		div3.append(off,pr,include);
		div3.setAttribute("class","div3");

		let main = el("div")
		main.append(div1,div2,div3);
		main.setAttribute("class","con");
		container.append(main);
	})
}

